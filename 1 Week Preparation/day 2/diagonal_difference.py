def diagonalDifference(arr):
    # Write your code here
    # HackerRank doesn't support Numpy 
    # but I'm using this bcs that's how I solved it
    diag1 = arr.diagonal().sum()
    diag2 =  np.flipud(arr).diagonal().sum()

    return abs(diag1 - diag2)


